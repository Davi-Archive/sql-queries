DECLARE @VALORINICIAL INT
DECLARE @VALORFINAL INT

SET @VALORINICIAL = 1
SET @VALORFINAL = 20

WHILE @VALORINICIAL<=@VALORFINAL
BEGIN
	PRINT 'O VALOR DO CONTADOR É: '+CONVERT(VARCHAR,@VALORINICIAL)
	SET @VALORINICIAL +=1
END
